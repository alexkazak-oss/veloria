export function getGamesByCategory(category: GameCategory): Game[] {
	return games.filter((g) => g.category === category)
}

export function getGamesByProvider(providerSlug: string): Game[] {
	return games.filter((g) => g.providerSlug === providerSlug)
}

export function getFeaturedGames(limit = 8): Game[] {
	return games.filter((g) => g.tags.includes('featured')).slice(0, limit)
}

export function getHotGames(limit = 8): Game[] {
	return games.filter((g) => g.tags.includes('hot')).slice(0, limit)
}

export function getLiveGames(limit = 6): Game[] {
	return games.filter((g) => g.isLive).slice(0, limit)
}
